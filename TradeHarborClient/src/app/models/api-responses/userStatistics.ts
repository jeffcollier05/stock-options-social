/**
 * Represents authenticated user's statistics.
 */
export class UserStatistics {
    /**
     * The number of friends associated with the user.
     */
    friendCount: number = 0;
  
    /**
     * The number of posts created by the user.
     */
    postCount: number = 0;
  
    /**
     * The number of upvotes received for the user.
     */
    upvotes: number = 0;
  
    /**
     * The number of downvotes received for the user.
     */
    downvotes: number = 0;
  }
  