/**
 * Represents user registration.
 */
export class Register {
    /**
     * The email for the new user's account.
     */
    email: string = '';
  
    /**
     * The desired username for the new user's account.
     */
    username: string = '';
  
    /**
     * The password for the new user's account.
     */
    password: string = '';
  
    /**
     * The first name of the new user.
     */
    firstName: string = '';
  
    /**
     * The last name of the new user.
     */
    lastName: string = '';
  
    /**
     * The URL for the profile picture of the new user.
     */
    profilePictureUrl: string = '';
  }
  